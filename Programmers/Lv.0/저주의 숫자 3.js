// 3이 포함되지 않는 조건과 3으로 나눠 떨어지지 않는 조건을 or연산자(||)로 처리하면 될거라고 생각했는데
// 계속 통과가 안되서 생각해보니까 and연산자(&&)로 처리해야 두 가지 조건 모두 배열에 원소로 들어가지 않는것,,
// 생각을 더 하고 문제를 풀쟈..

console.log(solution(15)); // 25
